  <h3>Karijera</h3><br>
  <div class="row">
    <div class="col-sm-12" >
      <p >Tarantinov prvi veliki uspjeh bila je prodaja scenarija pod naslovom Prava romansa, koji je napisao zajedno s Rogerom Avaryjem, i po kojem je snimljen film s Patriciom Arquette i Christianom Slaterom u glavnim ulogama. Njegov je i originalni scenario za Rođeni ubojice, koji je zapravo dio dužega scenarija iz kojega je potekao onaj za Pravu romansu, iako su ga kasnije znatno izmijenili drugi pisci.

Prodaja Prave romanse (1993.) pobudila je zanimanje za njegovu nadarenost. Upoznao je Lawrencea Bendera na jednoj od holivudskih zabava, i Bender je potakao Quentina da napiše film. Rezultat je bio Reservoir Dogs. Redatelj Monte Helman pročitao je scenarij i pomogao osigurati sredstva kompanije Live Entertainment, kao i Quentinov redateljski honorar. Pun stila, ovaj duhovit i krvlju natopljen pljačkaški film odredio je ugođaj njegovih kasnijih radova.

Slijedio je Pakleni šund, dobitnik Zlatne palme (Palme d'Or) na Filmskom festivalu u Cannesu 1994. Bio je to film sa zamršenim zapletom i podjednako brutalnim humorom. Mnoge uloge iz Paklenog šunda kritičari su oduševljeno pozdravili, a izvedba Johna Travolte zabilježena je kao ponovno oživljavanje njegove karijere.

Tarantinov sljedeći film bio je Jackie Brown (1997.), adaptacija romana Elmorea Leonarda, njegova mentora. U ovome djelu napravljenom u počast tzv. blaxploitation filmovima glumila je Pam Grier, glumica koja se pojavljuje u mnogim filmovima toga žanra sedamdesetih godina dvadesetog stoljeća.

1998. Quentin se posvetio brodvejskoj pozornici, gdje je glumio u predstavi Čekaj dok se smrači (engl.: Wait Until Dark).

U to je vrijeme planirao napraviti ratni film Nemilosrdni gadovi, koji je odgodio da bi napisao i režirao Kill Bill (izdan u dva dijela, Vol. 1 i Vol. 2), film osvete visokoga stila u duhu Wuxia filma kineske kinematografije and japanske filmske tradicije..</p>
